    parser.add_argument("--resume", action="store_true",
                        help="If final OPES outputs already exist, skip that system.")
    args = parser.parse_args()

    n_sigma = len([p for p in args.opes_sigma.split(",") if p.strip()])
    if args.bias_cv == "2d" and n_sigma != 2:
        parser.error("--bias-cv 2d requires exactly two comma-separated values in --opes-sigma")
    if args.bias_cv == "3d" and n_sigma != 3:
        parser.error("--bias-cv 3d requires exactly three comma-separated values in --opes-sigma")
