def _write_merged_campaign_log(out_root: Path, new_entries: list) -> None:
    """Merge *new_entries* into ``00_reference_data_log.json`` (locked, multi-process safe).

    Parallel ``--cases N`` runs share the same ``--out`` root; each process merges
    completed rows without clobbering siblings.
    """
    import fcntl

    log_path = out_root / "00_reference_data_log.json"
    log_path.parent.mkdir(parents=True, exist_ok=True)

    with open(log_path, "a+", encoding="utf-8") as fh:
        fcntl.flock(fh.fileno(), fcntl.LOCK_EX)
        try:
            fh.seek(0)
            raw = fh.read()
            prev: list = []
            if raw.strip():
                try:
                    loaded = json.loads(raw)
                    if isinstance(loaded, list):
                        prev = loaded
                except json.JSONDecodeError:
                    prev = []

            by_case: dict[str, dict] = {}
            for e in prev:
                if isinstance(e, dict) and isinstance(e.get("case"), str):
                    by_case[e["case"]] = e
            for e in new_entries:
                if isinstance(e, dict) and isinstance(e.get("case"), str):
                    by_case[e["case"]] = e

            ordered: list = []
            for sys_def in _SYSTEMS:
                name = sys_def["name"]
                if name in by_case:
                    ordered.append(by_case[name])

            payload = json.dumps(ordered, indent=2) + "\n"
            fh.seek(0)
            fh.truncate()
            fh.write(payload)
            fh.flush()
        finally:
            fcntl.flock(fh.fileno(), fcntl.LOCK_UN)


def _boltz_prep_for_openmm(
    yaml_path: Path,
    out_dir: Path,
    cache: Path,
    device: str,
    *,
    init_sampling_steps: int,
    recycling_steps: int,
):
    """Preprocess inputs, load Boltz-2, and return a bundle plus topology NPZ path.

    The processed ``structures/*.npz`` defines OpenMM–Boltz atom ordering but
    often has placeholder (zero) coordinates.  Call
    :func:`~genai_tps.backends.boltz.inference.predict_sample_atom_coords_numpy`
    on the returned bundle to obtain a self-consistent 3D pose for MD.
    """
    import torch

    from genai_tps.backends.boltz.inference import build_boltz_inference_session

    prep_dir = out_dir / "boltz_prep"
    bundle = build_boltz_inference_session(
        yaml_path=yaml_path,
        cache=cache,
        boltz_prep_dir=prep_dir,
        device=torch.device(device),
        diffusion_steps=int(init_sampling_steps),
        recycling_steps=int(recycling_steps),
        kernels=False,
    )
    topo_npz = bundle.topo_npz
    if topo_npz is None or not topo_npz.is_file():
        raise RuntimeError(
            f"Boltz preprocessing produced no topology NPZ under {prep_dir}. "
            "Check that the YAML and Boltz cache are correct."
        )
    print(f"  [prep] Topology NPZ: {topo_npz}", flush=True)
    return bundle, topo_npz


def _run_openmm_md(
    *,
    topo_npz: Path,
    frame_npz: Path | None,
    mol_dir: Path,
    out_dir: Path,
    n_steps: int,
    save_every: int,
    deposit_pace: int,
    opes_barrier: float,
    opes_biasfactor: float,
    opes_sigma: str,
    temperature: float,
    pocket_radius: float,
    platform: str,
    minimize_steps: int,
    progress_every: int,
    save_opes_every: int,
    opes_restart: Path | None,
    opes_mode: str,
    opes_kernel_cutoff: float | None,
    opes_nlist_parameters: tuple[float, float] | None,
    plumed_colvar_heavy_flush: bool,
    openmm_device_index: int | None = None,
    log_gpu_util: bool = False,
    gpu_util_interval: float = 0.1,
    gpu_util_out: Path | None = None,
    bias_cv: str = "2d",
    opes_explore: bool = False,
    opes_wall_dist: float | None = None,
    opes_wall_kappa: float = 200.0,
    coordination_r0: float = 4.5,
    opes_expanded_temp_max: float | None = None,
    opes_expanded_pace: int = 50,
    oneopes_axis_p0_boltz: list[int] | None = None,
    oneopes_axis_p1_boltz: list[int] | None = None,
    oneopes_contact_pairs_boltz: list[tuple[int, int]] | None = None,
    oneopes_hydration_boltz: list[int] | None = None,
    oneopes_water_oxygen_plumed: list[int] | None = None,
    oneopes_water_pace: int = 40_000,
    oneopes_water_barrier: float = 3.0,
    oneopes_water_biasfactor: float = 5.0,
    oneopes_water_sigma: float = 0.15,
    oneopes_auto_hydration: bool = True,
    oneopes_hydration_max_sites: int = 5,
    oneopes_hydration_use_3drism: bool = True,
    oneopes_hydration_min_density: float = 0.5,
) -> None:
    """Invoke the OpenMM OPES-MD runner via subprocess for isolation."""
    md_script = _REPO_ROOT / "scripts" / "run_openmm_opes_md.py"
    cmd = [
        sys.executable, str(md_script),
        "--topo-npz", str(topo_npz),
        "--out", str(out_dir),
        "--n-steps", str(n_steps),
        "--save-every", str(save_every),
        "--deposit-pace", str(deposit_pace),
        "--opes-barrier", str(opes_barrier),
        "--opes-biasfactor", str(opes_biasfactor),
        "--opes-sigma", opes_sigma,
        "--temperature", str(temperature),
        "--pocket-radius", str(pocket_radius),
        "--platform", platform,
        "--minimize-steps", str(minimize_steps),
        "--progress-every", str(progress_every),
        "--save-opes-every", str(save_opes_every),
        "--opes-mode", opes_mode,
        "--mol-dir", str(mol_dir),
        "--bias-cv", str(bias_cv),
    ]
    if opes_explore:
        cmd.append("--opes-explore")
    if opes_wall_dist is not None:
        cmd += ["--opes-wall-dist", str(float(opes_wall_dist))]
        cmd += ["--opes-wall-kappa", str(float(opes_wall_kappa))]
    cmd += ["--coordination-r0", str(float(coordination_r0))]
    if opes_expanded_temp_max is not None:
        cmd += [
            "--opes-expanded-temp-max",
            str(float(opes_expanded_temp_max)),
            "--opes-expanded-pace",
            str(int(opes_expanded_pace)),
        ]
    if opes_restart is not None:
        cmd += ["--opes-restart", str(opes_restart)]
    if opes_kernel_cutoff is not None:
        cmd += ["--opes-kernel-cutoff", str(opes_kernel_cutoff)]
    if opes_nlist_parameters is not None:
        a, b = opes_nlist_parameters
        cmd += ["--opes-nlist-parameters", f"{a},{b}"]
    if frame_npz is not None:
        cmd += ["--frame-npz", str(frame_npz)]
    if plumed_colvar_heavy_flush:
        cmd += ["--plumed-colvar-heavy-flush"]
    if platform in ("CUDA", "OpenCL") and openmm_device_index is not None:
        cmd += ["--openmm-device-index", str(int(openmm_device_index))]
    if log_gpu_util:
        cmd.append("--log-gpu-util")
        cmd += ["--gpu-util-interval", str(float(gpu_util_interval))]
    if gpu_util_out is not None:
        cmd += ["--gpu-util-out", str(gpu_util_out)]

    if bias_cv == "oneopes":
        if oneopes_axis_p0_boltz is not None:
            cmd += [
                "--oneopes-axis-p0-boltz",
                ",".join(str(int(x)) for x in oneopes_axis_p0_boltz),
            ]
        if oneopes_axis_p1_boltz is not None:
            cmd += [
                "--oneopes-axis-p1-boltz",
                ",".join(str(int(x)) for x in oneopes_axis_p1_boltz),
            ]
        if oneopes_contact_pairs_boltz is not None:
            pair_str = ",".join(f"{int(p)}-{int(l)}" for p, l in oneopes_contact_pairs_boltz)
            cmd += ["--oneopes-contact-pairs-boltz", pair_str]
        if oneopes_hydration_boltz is not None and len(oneopes_hydration_boltz) > 0:
            cmd += [
                "--oneopes-hydration-boltz",
                ",".join(str(int(x)) for x in oneopes_hydration_boltz),
            ]
        if oneopes_water_oxygen_plumed is not None and len(oneopes_water_oxygen_plumed) > 0:
            cmd += [
                "--oneopes-water-oxygen-plumed",
                ",".join(str(int(x)) for x in oneopes_water_oxygen_plumed),
            ]
        cmd += [
            "--oneopes-water-pace",
            str(int(oneopes_water_pace)),
            "--oneopes-water-barrier",
            str(float(oneopes_water_barrier)),
            "--oneopes-water-biasfactor",
            str(float(oneopes_water_biasfactor)),
            "--oneopes-water-sigma",
            str(float(oneopes_water_sigma)),
            "--oneopes-hydration-max-sites",
            str(int(oneopes_hydration_max_sites)),
            "--oneopes-hydration-min-density",
            str(float(oneopes_hydration_min_density)),
        ]
        if not oneopes_auto_hydration:
            cmd.append("--no-oneopes-auto-hydration")
        if not oneopes_hydration_use_3drism:
            cmd.append("--no-oneopes-hydration-use-3drism")

    print(f"  [md] Command: {' '.join(cmd)}", flush=True)
    result = subprocess.run(cmd, check=False, env=child_env_with_repo_src_python())
    if result.returncode != 0:
        raise RuntimeError(
            f"OpenMM OPES-MD failed with exit code {result.returncode}. "
            "See stderr above for details."
        )


